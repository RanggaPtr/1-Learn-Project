# tipe data satuan tanpa koma
data_integer=10
print("data :",data_integer,"type :",type(data_integer))

# tipe data satuan dengan koma
data_float=1.5
print("data :",data_float,"type :",type(data_float))

# tipe data string
data_string="Hello"
print("data :",data_string,"type :",type(data_string))

# tipe data boolean
data_boolean=True
print("data :",data_boolean,"type :",type(data_boolean))

# tipe data list
data_list=[1,2,3]
print("data :",data_list,"type :",type(data_list))

# tipe data dictionary
data_dictionary={1:"satu",2:"dua"}
print("data :",data_dictionary,"type :",type(data_dictionary))

# tipe data complex
data_complex=(1+2j)
print("data :",data_complex,"type :",type(data_complex))

# tipe dari data c 
from ctypes import c_double
data_c_double=c_double(1.5)
print("data :",data_c_double,"type :",type(data_c_double))
