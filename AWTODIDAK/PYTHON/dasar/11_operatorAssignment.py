# operator assignment

a=10 # ini adalah assignment
print(a)

# dibawah ini adalah operator assignment
a+=5 # artinya a=a+5
print("a+=5 = ",a)

a-=5 # artinya a=a-5
print("a-=5 = ",a)

a*=5 # artinya a=a*5
print("a*=5 = ",a)

a/=5 # artinya a=a/5
print("a/=5 = ",a)


a//=5 # artinya a=a//5
print("a//=5 = ",a)

a**=5 # artinya a=a**5
print("a**=5 = ",a)

a%=5 # artinya a=a%5
print("a%=5 = ",a)


