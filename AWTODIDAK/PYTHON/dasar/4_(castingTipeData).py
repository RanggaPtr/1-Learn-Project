# merubah 1 tipe ke tipe lain
# tipe data = int, float, str, bool

# data awal INT
print("===INT===")
data_int=9
print("data :",data_int,"type :",type(data_int))
# casting int to float
data_float=float(data_int)
print("data :",data_float,"type :",type(data_float))
# casting int to string
data_string=str(data_int)
print("data :",data_string,"type :",type(data_string))
# casting int to boolean (0 = False, 1 = True)
data_boolean=bool(data_int)
print("data :",data_boolean,"type :",type(data_boolean))

# data awal FLOAT
print("===FLOAT===")
data_float=1.9
print("data :",data_float,"type :",type(data_float))
# casting float to int akan dibulatkan kebawah 9.9 = 9 
data_int=int(data_float)
print("data :",data_int,"type :",type(data_int))
# casting float to string
data_string=str(data_float)
print("data :",data_string,"type :",type(data_string))
# casting float to boolean
data_boolean=bool(data_float)
print("data :",data_boolean,"type :",type(data_boolean))

# data awal STRING
print("===STRING===")
data_string=" 10 "
print("data :",data_string,"type :",type(data_string))
# casting string to int
data_int=int(data_string)
print("data :",data_int,"type :",type(data_int))
# casting string to float
data_float=float(data_string)
print("data :",data_float,"type :",type(data_float))
# casting string to boolean
data_boolean=bool(data_string)
print("data :",data_boolean,"type :",type(data_boolean))

# data awal BOOLEAN
print("===BOOLEAN===")
data_boolean=False
print("data :",data_boolean,"type :",type(data_boolean))
# casting boolean to int
data_int=int(data_boolean)
print("data :",data_int,"type :",type(data_int))
# casting boolean to float
data_float=float(data_boolean)
print("data :",data_float,"type :",type(data_float))
# casting boolean to string
data_string=str(data_boolean)
print("data :",data_string,"type :",type(data_string))





