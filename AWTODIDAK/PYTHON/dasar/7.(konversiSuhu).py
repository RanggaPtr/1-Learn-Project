# konversi suhu celcius,reamur, fahrenheit, kelvin
# konversi celcius ke lain
celcius = float(input("masukkan suhu celcius :"))

# konversi celcius ke reamur
reamur = (4/5) * celcius
print(celcius,"C ke reamur :",reamur)

# konversi celcius ke fahrenheit
fahrenheit = ((9/5) * celcius) + 32
print(celcius,"C ke fahrenheit :",fahrenheit)

# konversi celcius ke kelvin
kelvin = celcius + 273
print(celcius,"C ke kelvin :",kelvin)


